<h4>Formar profesionistas con especializaci&oacute;n en &aacute;reas de Procesamiento de Se&ntilde;ales, y Electr&oacute;nica de Potencia, que logren incorporarse a instituciones de investigaci&oacute;n y desarrollo tecnol&oacute;gico, industrias de base tecnol&oacute;gica y docencia.</h4>
<h3>OBJETIVOS PARTICULARES:</h3>
<h4>&middot; Vincular la investigaci&oacute;n con las actividades de los sectores productivo, social, educativo, de investigaci&oacute;n y de servicios.</h4>
<h4>&middot; Formar personal capacitado en la evaluaci&oacute;n, dise&ntilde;o, uso y mantenimiento de sistemas  relacionados con Procesamiento de Se&ntilde;ales y Electr&oacute;nica de Potencia.</h4>
<h4>&middot; Realizar, analizar y evaluar estudios de impacto tecnol&oacute;gico, social y ambiental en sus diferentes modalidades  y relacionados  con  Procesamiento  de  Se&ntilde;ales y Electr&oacute;nica de Potencia.</h4>
