<h5>*Nota:&nbsp; Las asignaturas b&aacute;sicas ser&aacute;n asignadas de acuerdo al perfil de ingreso del estudiante</h5>
<h5 style="padding-left: 30px;">Las asignaturas optativas ser&aacute;n determinadas de acuerdo al proyecto de investigaci&oacute;n y la LGAC elegida</h5>
