<h2>PLAN DE ESTUDIOS</h2>
<h3>El Plan de Estudios de la Maestr&iacute;a en Ciencias en Ingenier&iacute;a Electr&oacute;nica se desarrolla en 4 semestres (dos a&ntilde;os) y se conforma de la siguiente manera:</h3>
<h4>4 Asignaturas B&aacute;sicas.</h4>
<h4>4 Asignaturas Optativas.</h4>
