<h2><strong>CARACTER&Iacute;STICAS DEL PROGRAMA</strong></h2>
<p><strong><br /></strong></p>
<h3>Nombre: Maestr&iacute;a en Ciencias en Ingenier&iacute;a Electr&oacute;nica</h3>
<h3>Clave: MCIE0-2011-02</h3>
