<h4>Obtener 500 puntos en el  examen TOEFL/ITP.</h4>
<h4>Desarrollar un trabajo individual de tesis.</h4>
<h4>Contar con  la autorizaci&oacute;n de su Comit&eacute; Tutorial para la impresi&oacute;n del documento  final.</h4>
<h4>Presentar la defensa de tesis (Examen de grado).</h4>
<h4>No tener adeudos  con la Instituci&oacute;n.</h4>
