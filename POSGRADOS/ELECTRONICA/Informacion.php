<p>&nbsp;</p>
<h2><strong>REQUISITOS DE INGRESO</strong></h2>
<h3>Para ingresar a la Maestr&iacute;a en Ciencias en Ingenier&iacute;a Electr&oacute;nica se debe:</h3>
<h4>Estar titulado de licenciatura en Ingenier&iacute;a Electr&oacute;nica, Ingenier&iacute;a   El&eacute;ctrica, Ingenier&iacute;a en Sistemas Computacionales o &aacute;rea af&iacute;n, con   promedio m&iacute;nimo de 80/100.</h4>
<h4>Aprobar un examen de conocimientos b&aacute;sicos de ingenier&iacute;a o aprobar el curso proped&eacute;utico.</h4>
<ul>
</ul>
<h4>Obtener 450 puntos en el examen de ingl&eacute;s TOEFL/ITP.</h4>
<h4>Aprobar la entrevista personal con un comit&eacute; de Admisi&oacute;n.</h4>
<ul>
</ul>
<h4>Entregar dos cartas de recomendaci&oacute;n acad&eacute;mica, dirigidas al jefe de la DEPI y signadas por especialistas en el &aacute;rea.</h4>
<h4>Realizar el pago correspondiente.</h4>
