<h2>Perfil Del Graduado</h2>
<h3>El perfil del egresado de la Maestr&iacute;a en Ciencias en Ingenier&iacute;a  Electr&oacute;nica est&aacute; en concordancia con las actividades que desarrollar&aacute; y  las actitudes y aptitudes que requerir&aacute; para lograrlo, con lo cual:</h3>
<h3>&middot; Estar&aacute; capacitado para conocer, comprender y analizar problemas  relacionados con &aacute;reas de Procesamiento de Se&ntilde;ales y Electr&oacute;nica de  Potencia.</h3>
<h3>&middot;  Tendr&aacute; los conocimientos para abordar la problem&aacute;tica de impacto  tecnol&oacute;gico en el &aacute;mbito industrial, social, educativo, de investigaci&oacute;n  y de servicios.</h3>
<h3>&middot; Contar&aacute; con las bases te&oacute;ricas y pr&aacute;cticas que le permitan  interrelacionarse con profesionistas de otras ramas en la soluci&oacute;n  sustentable de problemas de impacto tecnol&oacute;gico, social y ambiental.</h3>
<h2>Campo de acci&oacute;n</h2>
<h3>La Maestr&iacute;a en Ciencias en Ingenier&iacute;a Electr&oacute;nica del Instituto  Tecnol&oacute;gico de Morelia cuenta con dos L&iacute;neas Generadoras del  Conocimiento: Procesamiento de Se&ntilde;ales y Electr&oacute;nica de Potencia.</h3>
<h3>La Electr&oacute;nica de Potencia es indispensable en el sector industrial  sea &eacute;ste primario, secundario o terciario, ya que en cualquier proceso  industrial se requiere el uso adecuado y eficaz de la energ&iacute;a, y esta  &aacute;rea del conocimiento trata de la conversi&oacute;n de la energ&iacute;a, la calidad  en la energ&iacute;a y el uso eficiente de los sistemas de potencia.</h3>
<h3>El Procesamiento de Se&ntilde;ales se requiere en unidades hospitalarias,  empresas tecnol&oacute;gicas, centros de investigaci&oacute;n y los diversos  organismos que se integran en los sectores industriales, educativos, de  salud y de servicios.</h3>
<p>&nbsp;</p>
