// JavaScript Document
function showDelPrograma(){
	document.getElementById('delprograma').style.display ='block';
	document.getElementById('deapoyo').style.display ='none';
	document.getElementById('coordinador').style.display ='none';
}

function showDeApoyo(){
	document.getElementById('delprograma').style.display ='none';
	document.getElementById('deapoyo').style.display ='block';
	document.getElementById('coordinador').style.display ='none';
}

function showCoordinador(){
	document.getElementById('delprograma').style.display ='none';
	document.getElementById('deapoyo').style.display ='none';
	document.getElementById('coordinador').style.display ='block';
}