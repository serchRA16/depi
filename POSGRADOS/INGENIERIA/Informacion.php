<h2>Desarrollar un trabajo individual de tesis.</h2>
</li>
<li>
<h2>Tener al menos 1 publicaci&oacute;n en el indice JCR y 1 submitida al solicitar el examen de grado.</h2>
</li>
<li>
<h2>Obtener 550 puntos en el examen TOEFL/ITP.</h2>
</li>
<li>
<h2>Contar con la autorizaci&oacute;n de su Comit&eacute; Tutorial para la impresi&oacute;n del documento final.</h2>
</li>
<li>
<h2>No tener adeudos con la Instituci&oacute;n.</h2>
</li>
<li>
<h2>Presentar la defensa de tesis (Examen de grado).&nbsp;</h2>
</li>
</ol>
