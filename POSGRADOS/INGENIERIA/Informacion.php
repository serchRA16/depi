<h1 style="text-align: center;"><span style="text-decoration: underline;"><strong>Caracter&iacute;sticas</strong></span></h1>
<h1>Nombre: Doctorado en Ciencias de la Ingenier&iacute;a</h1>
<h1>Clave: DCI-2010-13</h1>
<h2 style="text-align: left;">OBJETIVO GENERAL:</h2>
<h2 style="text-align: justify;">Formar profesionistas con especializaci&oacute;n en &aacute;reas de Tecnolog&iacute;as de los Materiales y Ciencias Metal&uacute;rgicas y Tecnolog&iacute;as de la Electr&oacute;nica, que logren incorporarse a instituciones de investigaci&oacute;n y desarrollo tecnol&oacute;gico, industrias de base tecnol&oacute;gica y docencia.</h2>
<h2 style="text-align: left;">Objetivos Particulares:</h2>
<h2 style="text-align: justify;">&middot; Vincular la investigaci&oacute;n con las actividades de los sectores productivo, social, educativo, de investigaci&oacute;n y de servicios.</h2>
<h2 style="text-align: justify;">&middot; Formar personal capacitado en la evaluaci&oacute;n, dise&ntilde;o, uso y mantenimiento de sistemas  relacionados con Tecnolog&iacute;as de los Materiales y Ciencias Metal&uacute;rgicas.</h2>
<h2 style="text-align: justify;">&middot; Formar personal capacitado en la evaluaci&oacute;n, dise&ntilde;o, uso y mantenimiento de sistemas  electr&oacute;nicos relacionados con Tecnolog&iacute;as de la Electr&oacute;nica.</h2>
<h2 style="text-align: justify;">&middot; Realizar, analizar y evaluar estudios de impacto tecnol&oacute;gico, social y ambiental en sus diferentes modalidades  y relacionados  con  Tecnolog&iacute;as de los Tratamientos T&eacute;rmicos y Tecnolog&iacute;as de la Electr&oacute;nica.</h2>
<h2 style="text-align: center;"><span style="text-decoration: underline;">Plan de Estudios</span></h2>
<h2 style="text-align: justify;">El Plan de Estudios del Doctorado en Ciencias de la Ingenier&iacute;a se desarrolla en 8 semestres (cuatro a&ntilde;os) y se conforma de la siguiente manera:</h2>
<ul>
<li>
<h2>1 Seminario de Investigaci&oacute;n.</h2>
</li>
<li>
<h2>5 cursos de Proyecto de Investigaci&oacute;n.</h2>
</li>
<li>
<h2>1 Seminario Predoctoral</h2>
</li>
<li>
<h2>1 Examen Predoctoral</h2>
</li>
<li>
<h2>Tesis de grado, que se fundamenta en el desarrollo de un trabajo de investigaci&oacute;n</h2>
</li>
</ul>
<h2><a href="documentos/Reticulas11.pdf">Reticulas</a>&nbsp;<img src="imagen/DOWNLOAD.jpeg" alt="Descarga" width="46" height="46" /></h2>
<h2 style="text-align: center;"><span style="text-decoration: underline;">Perfil del Graduado</span></h2>
<h2 style="text-align: justify;">El perfil del egresado del Doctorado en Ciencias en Ingenier&iacute;a est&aacute; en concordancia con las actividades que desarrollar&aacute; y las actitudes y aptitudes que requerir&aacute; para lograrlo, con lo cual:</h2>
<h2 style="text-align: justify;">&middot; Estar&aacute; capacitado para conocer, comprender y analizar problemas relacionados con &aacute;reas de Tecnolog&iacute;as de los Materiales y Ciencias Metal&uacute;rgicas y Tecnolog&iacute;as de la Electr&oacute;nica.</h2>
<h2 style="text-align: justify;">&middot;  Tendr&aacute; los conocimientos para abordar la problem&aacute;tica de impacto tecnol&oacute;gico en el &aacute;mbito industrial, social, educativo, de investigaci&oacute;n y de servicios.</h2>
<h2 style="text-align: justify;">&middot; Contar&aacute; con las bases te&oacute;ricas y pr&aacute;cticas que le permitan interrelacionarse con profesionistas de otras ramas en la soluci&oacute;n sustentable de problemas de impacto tecnol&oacute;gico, social y ambiental.</h2>
<h2 style="text-align: center;"><span style="text-decoration: underline;">Campo de acci&oacute;n</span></h2>
<h2 style="text-align: justify;">El Doctorado en Ciencias en Ingenier&iacute;a del Instituto Tecnol&oacute;gico de Morelia cuenta con dos L&iacute;neas Generadoras del Conocimiento: Tecnolog&iacute;as de los&nbsp; Materiales y Ciencias Metal&uacute;rgicas y Tecnolog&iacute;as de la Electr&oacute;nica.</h2>
<h2 style="text-align: justify;">Las Tecnolog&iacute;as de la Electr&oacute;nica son indispensables en el sector industrial sea &eacute;ste primario, secundario o terciario, ya que en cualquier proceso industrial se requiere el uso adecuado y eficaz de la energ&iacute;a, y esta &aacute;rea del conocimiento trata de la conversi&oacute;n de la energ&iacute;a, la calidad en la energ&iacute;a y el uso eficiente de los sistemas de potencia. En adici&oacute;n, se requieren en unidades hospitalarias, empresas tecnol&oacute;gicas, centros de investigaci&oacute;n y los diversos organismos que se integran en los sectores industriales, educativos de salud y de servicios.</h2>
<h2 style="text-align: center;"><span style="text-decoration: underline;">Requisitos de egreso</span></h2>
<h2>Para obtener el grado de Doctor en Ciencias de la Ingenier&iacute;a se debe:&nbsp;</h2>
<ol>
